/**
 * This is the interface for Employees with employee_id, first_name, last_name, phone_number
 */
export class Employees {
    /**
     * The id of the employee.
     */
    employee_id?: number;
    /**
     * The first name of the employee.
     */
    first_name?: string;
    /**
     *  The last name of the employee.
     */
    last_name?: string;
    /**
     * The phone number of the employee.
     */
    position?: string;
}
