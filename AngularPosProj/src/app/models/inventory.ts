/**
 * Represents an item in the inventory, including its ID, name, and quantity.
 */
 export class Inventory {
    /**
     * The id of the item.
     */
    item_id?: number;
    /**
     * The name of the item.
     */
    item_name?: string;
    /**
     * The quantity of the item.
     */
    item_quantity?: number;
}